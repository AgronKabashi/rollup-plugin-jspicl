function generateCartridge (lua, cartridgePath) {
  const {
    gff = defaultGff,
    gfx = defaultGfx,
    music = defaultMusic,
    map = defaultMap,
    sfx = defaultSfx
  } = getCartridgeDetails(cartridgePath);

  return `pico-8 cartridge // http://www.pico-8.com
version 8
__lua__
${lua}
__gfx__
${gfx}
__gff__
${gff}
__map__
${map}
__sfx__
${sfx}
__music__
${music}

`;
}

function getCartridgeDetails (cartridgePath) {
  const result = {};

  try {
    const contents = fs.readFileSync(path.resolve(cartridgePath), { encoding: "utf8" });

    let content, section;

    // Extract the contents of each section
    const regex = /__([a-z]+)__\n([\s\S]*?)(?=\n__\w+__\n|\n(\n|$))/g;
    while ([, section, content] = regex.exec(contents) || "") { // eslint-disable-line no-cond-assign
      if (section !== "lua") {
        result[section] = content;
      }
    }
  }
  catch (error) {
    // File probably doesn't exist
    console.warn("Warning: Could not parse cartridge or no cartridge found"); // eslint-disable-line no-console
  }

  return result;
}

// Each token is a word (e.g. variable name) or
// operator. Pairs of brackets, and strings count as 1 token. commas, periods, LOCALs, semi-
// colons, ENDs, and comments are not counted.
/* eslint-disable no-multi-spaces */
const tokens = [
  // general
  "\"[^\"]*\"",       // Strings
  "\\d+\\.\\d+",      // floating numbers
  "\\w+",             // words
  "\\d+",             // numbers

  "!=",               // inequality
  "==",               // comparison
  "\\+=",             // incrementing assignment
  "-=",               // decrementing assignment
  "<=",               // equal or less than
  ">=",               // equal or greater than
  "\\.\\.",           // string concatenation

  "<",                // less than
  ">",                // greater than
  "\\+",              // addition
  "-",                // subtraction
  "\\/",              // division
  "\\*",              // multiplication
  "=",                // equals
  "\\%",              // percentage
  "\\(",              // paranthesis
  "\\[",              // left bracket
  "\\{"               // left curly brace
].join("|");

const regex = new RegExp(`(${tokens})`, "gi");

function tokenCounter (luaCode) {
  return luaCode.match(regex)
    .filter(token => token !== "local" && token !== "end")
    .length;
}

function logToFile (content, filePath) {
  mkdirp.sync(path.dirname(filePath));
  fs.writeFileSync(path.resolve(filePath), content);
}

function logStats (lua, cartridge) {
  const tokens = tokenCounter(lua);

  const stats = [
    {
      label: "Characters",
      value: lua.length,
      percent: `${~~(lua.length * 100 / 65535)}%`
    },
    {
      label: "Tokens",
      value: `~${tokens}`,
      percent: `${~~(tokens * 100 / 8192)}%`
    },
    {
      label: "Filesize",
      value: `${Math.ceil(cartridge.length / 1024)} KB`
    }
  ];

  console.log(columnify(stats, { // eslint-disable-line no-console
    minWidth: 10,
    align: "right",
    dataTransform: value => `\x1b[33m${value}\x1b[0m`,
    headingTransform: () => "",
    config: {
      label: {
        align: "left",
        minWidth: 12,
        dataTransform: value => `\x1b[32m${value}\x1b[0m`
      }
    }
  }));
}

function index (customizedOptions) {
  const options = Object.assign({}, defaultOptions, customizedOptions);

  if (!options.cartridgePath) {
    throw new Error("Ensure that 'cartridgePath' property in options is set.");
  }

  const picoOptions = Object.assign({}, defaultPicoOptions, options.pico);

  let picoProcess = null;

  return {
    transformBundle: javascriptCode => {
      const { cartridgePath, luaOutput, jsOutput, showStats } = options;

      const { output, polyfills } = jspicl(javascriptCode);
      const jspiclBanner = options.includeBanner && banner || "";
      const luaCode = `${jspiclBanner} ${polyfills} ${output}`;
      const cartridge = generateCartridge(luaCode, cartridgePath);

      jsOutput && logToFile(javascriptCode, jsOutput);
      luaOutput && logToFile(luaCode, luaOutput);
      showStats && logStats(luaCode, cartridge);

      return cartridge;
    },

    ongenerate: ({ file }) => {
      if (!picoOptions.autoRun) {
        return;
      }

      if (picoProcess) {
        if (!picoOptions.reloadOnSave) {
          return;
        }

        // Currently only MacOS supports auto reloading when saving.
        process.platform === "darwin" && child_process.execSync(`osascript "${path.join(__dirname, "reload-pico8.scpt")}"`);
      }
      else {
        // Use customized path if available, otherwise fallback to the default one for the current OS
        const picoPath = picoOptions.customizedOptions || pico8PathMap[process.platform];

        picoProcess = child_process.spawn(picoPath, ["-run", `"${path.join(".", file)}"`], {
          shell: true,
          stdio: picoOptions.pipeOutputToConsole ? "inherit" : "pipe"
        });

        picoProcess.on("close", code => {
          picoProcess = null;
          console.log(`Pico-8 process exited with code ${code}`); // eslint-disable-line no-console
        });
      }
    }
  };
}

module.exports = index;
